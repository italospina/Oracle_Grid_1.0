===================================================================================
==================== Caso precise criar um modelo de dados ======================== 
== ------------------------------------------------------------------------------==
==Quando identifica uma entidade, ou cria entidades ou entidades mais abstratas. ==
==Voce deve determinar os relacionamentos desses dados seja de 1-1, 1-M, M-M.    ==
===================================================================================

===================================================================================
============= Como Converter o Modelo Físico em Modelo Lógico =====================
== Os passos para converter o modelo lógico para o físico são os seguintes:      ==
== ------------------------------------------------------------------------------==
==--Entidades são transformadas em tabelas                                       ==
==--Relacionamentos viram relações entre chave primária e                        ==
==--chave estrangeira (Primary Keys e Foreign Keys)                              ==
==                                                                               ==
==--Atributos Viram Colunas                                                      ==            
==--Faça adaptações no modelo físico conforme requisitos de negócio adicionais.  ==
===================================================================================
